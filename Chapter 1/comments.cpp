#include <iostream>

int main()
{
    std::cout << "Hello world!"; // This is a comment in line
    std::cout << "Hello world\n"; // std::cout is part of iostream

    
    // std::cout lives in the iostream library
    std::cout << "Hello world!\n";
    
    // this is much easier to read
    std::cout << "It is very nice to meet you!\n";
    
    // don't you think so?
    std::cout << "Yeah!\n";

    /*
     * This is a multiline comment
     */
    return 0;
}